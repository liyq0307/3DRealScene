namespace RealScene3D.Domain.Enums;

/// <summary>
/// 切片策略枚举 - 不同空间剖分算法的选择
/// 定义支持的切片算法类型，每种算法适用于不同的数据特征和使用场景
/// </summary>
public enum SlicingStrategy
{
    /// <summary>
    /// 规则网格切片算法
    /// 算法：均匀网格剖分，适用于规则分布的数据，计算简单，内存占用规律
    /// 优点：处理速度快，内存使用可预测
    /// 缺点：对不规则数据适应性差，可能产生过多冗余切片
    /// 使用场景：规则地形、城市网格数据
    /// </summary>
    Grid = 0,

    /// <summary>
    /// 八叉树切片算法（默认）
    /// 算法：层次递归剖分，适用于非均匀分布的数据，自适应精度，平衡细节和性能
    /// 优点：自适应精度，减少冗余切片，层次结构利于LOD
    /// 缺点：剖分算法较复杂，实现难度较高
    /// 使用场景：倾斜摄影、BIM模型、复杂地形
    /// </summary>
    Octree = 1,

    /// <summary>
    /// KD树切片算法
    /// 算法：基于方差的二分剖分，适用于高维空间查询优化，剖分轴交替选择
    /// 优点：最优空间查询性能，自适应数据分布
    /// 缺点：构造复杂度高，内存开销较大
    /// 使用场景：激光点云、海量空间数据、查询密集场景
    /// </summary>
    KdTree = 2,

    /// <summary>
    /// 自适应切片算法
    /// 算法：基于数据密度和几何特征的智能剖分，自动调整切片大小和LOD级别
    /// 优点：最佳质量和性能平衡，完全自适应
    /// 缺点：算法复杂度最高，实现和调试困难
    /// 使用场景：高精度需求、混合类型数据、质量优先场景
    /// </summary>
    Adaptive = 3,

    /// <summary>
    /// 递归空间剖分算法（类似Obj2Tiles）
    /// 算法：递归N×N剖分，支持动态剖分深度决策，集成QEM网格简化
    /// 优点：完整的LOD支持，基于几何密度自适应调整
    /// 缺点：需要独立模型加载，处理流程较复杂
    /// 使用场景：OBJ/GLTF转3D Tiles，需要真正网格简化的场景
    /// </summary>
    Recursive = 4
}

/// <summary>
/// 切片任务状态枚举
/// </summary>
public enum SlicingTaskStatus
{
    Created = 0,
    Queued = 1,
    Processing = 2,
    Completed = 3,
    Failed = 4,
    Cancelled = 5
}
